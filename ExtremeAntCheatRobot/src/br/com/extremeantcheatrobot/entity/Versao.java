package br.com.extremeantcheatrobot.entity;

public class Versao {

	private Integer id;
	private String ultimaVersao;
	private String novaVersao;
	private String descricaoVersao;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUltimaVersao() {
		return ultimaVersao;
	}
	public void setUltimaVersao(String ultimaVersao) {
		this.ultimaVersao = ultimaVersao;
	}
	public String getNovaVersao() {
		return novaVersao;
	}
	public void setNovaVersao(String novaVersao) {
		this.novaVersao = novaVersao;
	}
	public String getDescricaoVersao() {
		return descricaoVersao;
	}
	public void setDescricaoVersao(String descricaoVersao) {
		this.descricaoVersao = descricaoVersao;
	}
	
	
}
